#include "libft.h"
#include "stdlib.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_node;

	new_node = malloc(sizeof(t_list));
	if (!new_node)
		return (NULL);
	new_node->content = content;
	new_node->next = NULL;
	return (new_node);
}

// a node == data + pointer to next data
//  allocating memory in my new node for the size of my struct
// assigning my new node's values to the argument and
// the next adress to NULL since we only create one node
// returning value