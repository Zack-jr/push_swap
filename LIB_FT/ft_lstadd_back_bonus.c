#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (new == NULL)
		return ;
	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	last = *lst;
	while (last->next != NULL)
		last = last->next;
	last->next = new;
}

// goal = add a new node to the end of the linked list
// if node is null then add the current one and leave
// iterate with last until we find the end of the list
// setting the node at the end and putting the new node->next to NULL;